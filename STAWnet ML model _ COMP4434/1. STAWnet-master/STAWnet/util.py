def load_dataset(dataset_dir, batch_size, valid_batch_size=None, test_batch_size=None):
    """Load dataset and create data loaders
    
    Args:
        dataset_dir: Path to data directory
        batch_size: Batch size for training
        valid_batch_size: Batch size for validation
        test_batch_size: Batch size for testing
    """
    # Load train and test data
    train_data = np.load(f'{dataset_dir}/train.npz')
    test_data = np.load(f'{dataset_dir}/test.npz')
    
    # Create data loaders
    return {
        'train_loader': DataLoader(train_data['x'], train_data['y'], batch_size),
        'val_loader': DataLoader(train_data['x'], train_data['y'], valid_batch_size),
        'test_loader': DataLoader(test_data['x'], test_data['y'], test_batch_size)
    }

def masked_mse(preds, labels, null_val=np.nan):
    if np.isnan(null_val):
        mask = ~torch.isnan(labels)
    else:
        mask = (labels!=null_val)
    mask = mask.float()
    mask /= torch.mean((mask))
    mask = torch.where(torch.isnan(mask), torch.zeros_like(mask), mask)
    loss = (preds-labels)**2
    loss = loss * mask
    loss = torch.where(torch.isnan(loss), torch.zeros_like(loss), loss)
    return torch.mean(loss)

def masked_rmse(preds, labels, null_val=np.nan):
    return torch.sqrt(masked_mse(preds=preds, labels=labels, null_val=null_val))


def masked_mae(preds, labels, null_val=np.nan):
    """Calculate MAE with masking"""
    mask = (labels!=null_val).float()
    mask /= torch.mean(mask)
    
    # MAE calculation
    loss = torch.abs(preds-labels)
    loss = loss * mask
    return torch.mean(loss)


def masked_mape(preds, labels, null_val=np.nan):
    """Calculate MAPE with masking"""
    mask = (labels!=null_val).float()
    mask /= torch.mean(mask)
    
    # MAPE calculation
    loss = torch.abs(preds-labels)/labels
    loss = loss * mask
    return torch.mean(loss)


def metric(pred, real):
    mae = masked_mae(pred, real, 0.0).item()
    mape = masked_mape(pred, real, 0.0).item()
    rmse = masked_rmse(pred, real, 0.0).item()
    return mae, mape, rmse

